    시퀀스객체[-음수]  # 인덱스를 음수로 지정하면 뒤에서부터 요소에 접근, -1은 뒤에서 첫 번째
    시퀀스객체[인덱스] = 값  # 시퀀스 객체의 요소에 값 저장
    del 시퀀스객체[인덱스]  # 시퀀스 객체의 요소를 삭제

