시퀀스객체 * 정수  # 시퀀스 객체를 특정 횟수만큼 반복하여 새 시퀀스 객체를 만듦
정수 * 시퀀스객체  # 시퀀스 객체를 특정 횟수만큼 반복하여 새 시퀀스 객체를 만듦

len(시퀀스객체)  # 시퀀스 객체의 요소 개수(길이) 구하기
max(시퀀스객체)  # 시퀀스 객체중 최대값인 요소 구하기
min(시퀀스객체)  # 시퀀스 객체중 최소값인 요소 구하기

